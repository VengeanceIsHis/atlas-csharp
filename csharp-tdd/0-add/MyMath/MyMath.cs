namespace MyMath
{
    public class Operations
    {
        public static int Add(int a, int b)
        {
            /// <summary>
        /// Adds two integers.
        /// </summary>
        /// <param name="a">The first integer to add.</param>
        /// <param name="b">The second integer to add.</param>
        /// <returns>The sum of the two integers.</returns>
            return a + b;
        }
    }
}

